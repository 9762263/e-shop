package ru.unlimit;

public class User {

	public String name;
	public String email;
	public String phone;
	public String adres;
	public String passport;
	public String parol;
	
	public User(){
		
	}
	
	public User(String name, String email, String phone, String adres, String passport,String parol) {
		super();
		this.name = name;
		this.email = email;
		this.phone = phone;
		this.adres = adres;
		this.passport = passport;
		this.parol = parol;
	}
	
	
}
